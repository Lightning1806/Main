package com.Vishal;

import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
        for(int i = 1 ; i<=5; i +=1){
            System.out.println("hellow world");
        }
        int j=1;
        while (j<=5){
            System.out.println("j");
            j+=2 ;
        }
        int k = 1;
        do{
            System.out.println(k);
            k++;
        }while (k<=5);
    }
}
